-- Extensions
create extension if not exists "pgcrypto";

-- Tables
create table if not exists public.events (
  id uuid primary key default gen_random_uuid(),
  user_id uuid null,
  title text not null,
  description text null,
  date text not null,
  location text not null,
  location_url text null,
  image_url text null,
  slider_images text[] null,
  agenda jsonb null,
  theme_id text null,
  status text null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.attendees (
  id uuid primary key default gen_random_uuid(),
  event_id uuid null references public.events(id) on delete cascade,
  name text not null,
  email text not null,
  whatsapp_number text null,
  response text null,
  link_shared boolean null default false,
  created_at timestamptz not null default now()
);

create table if not exists public.short_urls (
  id bigint generated by default as identity primary key,
  short_id text not null,
  original_url text not null,
  created_at timestamptz not null default now(),
  expires_at timestamptz not null,
  clicks integer null default 0,
  constraint short_urls_short_id_key unique (short_id)
);

-- Indexes
create index if not exists attendees_event_id_idx on public.attendees(event_id);
create index if not exists short_urls_expires_at_idx on public.short_urls(expires_at);

-- updated_at trigger
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists set_events_updated_at on public.events;
create trigger set_events_updated_at
before update on public.events
for each row execute function public.set_updated_at();

-- Cleanup functions
create or replace function public.cleanup_expired_short_urls()
returns void
language plpgsql
as $$
begin
  delete from public.short_urls where expires_at < now();
end;
$$;

