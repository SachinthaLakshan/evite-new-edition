-- RLS
alter table public.events enable row level security;
alter table public.attendees enable row level security;
alter table public.short_urls enable row level security;

-- Policies: events
create policy "events_select_own" on public.events
for select
to authenticated
using (user_id = auth.uid());

create policy "events_insert_own" on public.events
for insert
to authenticated
with check (user_id = auth.uid());

create policy "events_update_own" on public.events
for update
to authenticated
using (user_id = auth.uid())
with check (user_id = auth.uid());

create policy "events_delete_own" on public.events
for delete
to authenticated
using (user_id = auth.uid());

-- Policies: attendees
create policy "attendees_select_event_owner" on public.attendees
for select
to authenticated
using (
  exists (
    select 1 from public.events e
    where e.id = attendees.event_id
      and e.user_id = auth.uid()
  )
);

create policy "attendees_insert_event_owner" on public.attendees
for insert
to authenticated
with check (
  exists (
    select 1 from public.events e
    where e.id = attendees.event_id
      and e.user_id = auth.uid()
  )
);

create policy "attendees_update_event_owner" on public.attendees
for update
to authenticated
using (
  exists (
    select 1 from public.events e
    where e.id = attendees.event_id
      and e.user_id = auth.uid()
  )
)
with check (
  exists (
    select 1 from public.events e
    where e.id = attendees.event_id
      and e.user_id = auth.uid()
  )
);

create policy "attendees_delete_event_owner" on public.attendees
for delete
to authenticated
using (
  exists (
    select 1 from public.events e
    where e.id = attendees.event_id
      and e.user_id = auth.uid()
  )
);

-- Policies: public RSVP read/update attendee by id (no auth)
create policy "attendees_public_read" on public.attendees
for select
to anon
using (true);

create policy "attendees_public_update_response" on public.attendees
for update
to anon
using (true)
with check (true);

-- Policies: public event read (no auth)
create policy "events_public_read" on public.events
for select
to anon
using (true);

-- Policies: short_urls public read/insert/update/delete (used by anon redirect)
create policy "short_urls_public_all" on public.short_urls
for all
to anon
using (true)
with check (true);

